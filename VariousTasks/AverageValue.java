package VariousTasks;

public class AverageValue {

    public int fundStateTrue(int first, int second) {
        int result = (first + second) / 2;
        return result;
    }

    public int fundStateFalse(int first, int second, int third) {
        int result = (first + second + third) / 3;
        return result;
    }
}



